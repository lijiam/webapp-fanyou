package com.webapp.fanyou.bean;

public class Stock {
    private String id;
    private String name;
    private String number;
    private String dofood_id;
    private String dofood_name;
    private String is_in;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDofood_id() {
        return dofood_id;
    }

    public void setDofood_id(String dofood_id) {
        this.dofood_id = dofood_id;
    }

    public String getDofood_name() {
        return dofood_name;
    }

    public void setDofood_name(String dofood_name) {
        this.dofood_name = dofood_name;
    }

    public String getIs_in() {
        return is_in;
    }

    public void setIs_in(String is_in) {
        this.is_in = is_in;
    }
}
