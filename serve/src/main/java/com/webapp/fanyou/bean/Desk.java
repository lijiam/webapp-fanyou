package com.webapp.fanyou.bean;

public class Desk {

    private String id;
    private String number;
    private String desk_type;
    private String desk_type_name;
    private String start_time;
    private String times;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDesk_type() {
        return desk_type;
    }

    public void setDesk_type(String desk_type) {
        this.desk_type = desk_type;
    }

    public String getDesk_type_name() {
        return desk_type_name;
    }

    public void setDesk_type_name(String desk_type_name) {
        this.desk_type_name = desk_type_name;
    }

    public String getStart_time() {
        return start_time;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public String getTimes() {
        return times;
    }

    public void setTimes(String times) {
        this.times = times;
    }
}
